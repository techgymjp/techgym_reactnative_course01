function sortAtRandom(cards) {
  const cardsLength = cards.length;
  const randomCards = [];
  for (let i = 0; i < cardsLength; i++) {
    let randomTarget = Math.floor(Math.random() * cards.length);
    randomCards[i] = cards[randomTarget];
    cards.splice(randomTarget, 1);
  }
  return randomCards;
}

const randomCards = sortAtRandom(Cards);

function createPyramid(cards) {
  const pyramid = [];
  for (let i = 0; i < 7; i++) {
    let thislayer = [];
    for (let j = 0; j < i + 1; j++) {
      thislayer[j] = cards[0];
      cards.splice(0, 1);
    }
    pyramid[i] = thislayer;
  }
  return pyramid;
}

